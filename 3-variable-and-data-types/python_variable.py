# What is a variable
var = "variable"
# var is the variable
# Result = "variable"

# Assign variable
var_1 = "variable"
print(var_1)
# Result var_1 = "variable"

# Re-assign variable
var_1 = "variable_1"
print(var_1)
# Result var_1 = "variable_1"

# Re-assign variable with different type of data
var_1 = 1
print(var_1)
# Result var_1 = 1

# Chain assign variable
var2, var_3 = 3.14
print(var2, var_3)
# Result var2 = 3.14
# Result var_3 = 3.14
