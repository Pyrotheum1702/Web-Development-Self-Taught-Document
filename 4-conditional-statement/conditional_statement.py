# If statement implementation
if 2 < 5:
    print(True)

# Result: 2 < 5 is True

# Else statement implementation
if 2 > 5:
    print(True)
else:
    print(False)
# Result: 2 < 5 is False

# Elif statement implementation
if 2 > 5:
    print(True)
elif 2 > 0:
    print(True)
# Result: 2 > 5 is False but 2 > 0 is True
